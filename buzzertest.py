import time
import RPi.GPIO as gpio

pin = 12

gpio.setmode(gpio.BCM)
gpio.setup(pin, gpio.OUT)

def play(freq, duration):
    start_time = time.time()
    while time.time() - start_time < duration:
        gpio.output(pin, 1)
        time.sleep(0.5 / freq)
        gpio.output(pin, 0)
        time.sleep(0.5 / freq)

